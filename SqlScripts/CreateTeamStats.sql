use NBA;
create table TeamStats(
	Season VARCHAR(50) NOT NULL,
    League VARCHAR(10) NOT NULL,
    TeamAbbr VARCHAR(5) NOT NULL,
    WINS INTEGER,
    LOSSES INTEGER,
    Finish INTEGER,
    Age NUMERIC(3,1),
    Height VARCHAR(4),
    Weight INTEGER,
    Games INTEGER,
    MP INTEGER,
    FG INTEGER,
    FGA INTEGER,
    FGP NUMERIC(4,3),
    3P INTEGER,
    3PA INTEGER,
    3PP NUMERIC(4,3),
    2P INTEGER,
    2PA INTEGER,
    2PP NUMERIC(4,3),
    FT INTEGER,
    FTA INTEGER,
    FTP NUMERIC(4,3),
    ORB INTEGER,
    DRB INTEGER,
    TRB INTEGER,
    AST INTEGER,
    STL INTEGER,
    BLK INTEGER,
    TOV INTEGER,
	PF INTEGER,
    PTS INTEGER,
    Primary Key (Season, TeamAbbr)
);